def count_numbers_in_ranges(input_string):
    ranges = input_string.split(',')
    total = 0


    for r in ranges:
        start, end = map(int, r.split('-'))
        for num in range(start, end + 1):
            s = str(num)
            if len(s) % 2 == 0:
                half =  len(s) // 2
                if s[:half] == s[half:]:
                    total += num
            else:
                continue
    return total

